'''
Numpy Financial tiene una funcion irr() utilizadea para calcular el IRR (INTERNAL RATE OF RETURN) O TAZA INTERNAA DE RETORNO.

IRR: ES EL PORCENTAJE BENEFICIO O PERDIDA QUE CONLLEVA CUALQUIER INVERSION.

VAMOS A ASUMIR QUE INVERTIMOS 5000 Y OBTUVIMOS LOS SIGUIENTES PAGOS: 500, 700, 1000, 3000.

PARA CALCULAR EL IRR, PRIMERO NECESITAMOS DECLARAR UN ARREGLO CON LOS VALORES, CON EL PRIMER VALOR COMIENZA NUESTRA INVERSION INICIAL:
'''
import numpy_financial as npf

cashflow = [-5000, 500, 700, 1000,3000]

#Ahora podemos usar la funcion irr()

print(npf.irr(cashflow))

'''
COMPARANDO IRR

VAMOS A USAR LA FUNCION irr() PARA COMPARAR DOS OPORTUNIDADES DE INVERSION Y DECIDIR CUAL ES LA MEJOR.

OPCION 1:

REQUERIMOS 50,000 EN INVERISON.
PAGAREMOS 10K,13K,25K,42K CADA Aﾃ前 DURANTE LOS SIGUIENTES 5 Aﾃ前S.

OPCION 2:

NECESITAMOS 30K EN INVERSION

PAGAREMOS 1K,13K,18K,25K,20K CADA Aﾃ前 POR LOS PROXIMOS 5 Aﾃ前S.

VAMOS A CALCULAR EL IRR PARA CADA INVERSION Y COMPARARLAS:
'''

cf1 = [-50000,10000,25000,35000,42000]

cf2 = [-30000,10000,13000,18000,25000,20000]

print('Opcion 1', npf.irr(cf1))

print('Opcion 2', npf.irr(cf2))
